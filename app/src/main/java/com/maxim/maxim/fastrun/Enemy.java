package com.maxim.maxim.fastrun;

public class Enemy {
    private int enemySize = 0;
    private int enemyPosX = 0;
    private int enemyPosY = 0;

    public int getEnemySize() {
        return enemySize;
    }

    public void setEnemySize(int enemySize) {
        this.enemySize = enemySize;
    }

    public int getEnemyPosX() {
        return enemyPosX;
    }

    public void setEnemyPosX(int enemyPosX) {
        this.enemyPosX = enemyPosX;
    }

    public int getEnemyPosY() {
        return enemyPosY;
    }

    public void setEnemyPosY(int enemyPosY) {
        this.enemyPosY = enemyPosY;
    }
}
